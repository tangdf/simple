package com.wb.qdyth.common.bo;



import java.util.Date;

import com.wb.jdbcutils.annos.Column;
import com.wb.jdbcutils.annos.Table;




@Table(name="NET_STATUS"
)
public class NetStatus {


     private Long lsh;
     private String link;
     private String clzt;
     private Date time;
     private String memo;
     private String undo;
     private String type;
     
 	@Column(id=true,name="lsh")
	public Long getLsh() {
		return lsh;
	}

	public void setLsh(Long lsh) {
		this.lsh = lsh;
	}

	@Column(name="LINK", length=3)
	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	@Column(name="CLZT", length=3)
	public String getClzt() {
		return clzt;
	}

	public void setClzt(String clzt) {
		this.clzt = clzt;
	}

	@Column(name="TIME")
	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

	@Column(name="MEMO")
	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	@Column(name="UNDO")
	public String getUndo() {
		return undo;
	}

	public void setUndo(String undo) {
		this.undo = undo;
	}
	
	@Column(name="TYPE")
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}


